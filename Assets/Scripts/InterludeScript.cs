    public static List<InterludeUI.Beat> GetBeats(string interludeId, string heroId)
    {
        var list = new List<InterludeUI.Beat>();
        if (string.IsNullOrEmpty(heroId)) heroId = "HeroAldin";

        // ローカルヘルパ：ト書き / ナレーション / セリフ。
        void Stage(string t) => list.Add(new InterludeUI.Beat { Kind = InterludeUI.BeatKind.Stage, Text = t });
        void Narr(string t) => list.Add(new InterludeUI.Beat { Kind = InterludeUI.BeatKind.Narration, Name = "ナレーション", Text = t });
        void L(string name, string t)
        {
            string id; bool hero;
            if (name == "主人公") { id = heroId; hero = true; name = LocalizationManager.UnitName(heroId); }
            else if (name == "カガチ犬") { id = "Skindogehai"; hero = true; name = "カガチ"; } // 犬化後：表示名はカガチ／立ち絵は犬化
            else if (NameMap.TryGetValue(name, out var v)) { id = v.Key; hero = v.Value; }
            else { id = ""; hero = false; }
            list.Add(new InterludeUI.Beat { Kind = InterludeUI.BeatKind.Speech, Name = name, PortraitId = id, Text = t, HeroSide = hero });
        }

        switch ((interludeId ?? "").ToUpperInvariant())
        {
            case "CHOPEN_1": BuildChapterOpen1(Stage, Narr, L); break;
            case "CHOPEN_2": BuildChapterOpen2(Stage, Narr, L); break;
            case "INT_01": BuildInt01(Stage, L); break;
            case "INT_08": BuildInt08(Stage, L); break;
            case "INT_12": BuildInt12(Stage, L); break;
            case "INT_13": BuildInt13(Stage, L, heroId); break;
            case "ENDING": BuildEnding(Stage, Narr, L, heroId); break;
        }
        return list;
    }

